console.log('Les boucles yay fun');

// * Boucle : effectuer une répétition
//      https://developer.mozilla.org/fr/docs/Web/JavaScript/Reference/Statements/for

console.log('Je suis avant la boucle');

// Pour i qui va de zéro à 10, on augmente de 1 à chaque tour
for(let i = 0 ; i < 10 ; i++ ) {
    console.log(`Je suis au tour ${i}`);
}

console.log('Je suis après la boucle');

